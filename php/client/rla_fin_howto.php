<h1>Steps to place an order on RLA Intranet</h1>
<h2><font color="#0000FF">Step 1: Check Supplier List</font></h2>
<ul>
  <li>&nbsp;&nbsp;&nbsp; If the supplier is on the supplier list please go to step
2. <font color="#FF0000">Remember a valid supplier should have fax and phone no.</font></li>
  <li><font color="#000000">&nbsp;&nbsp;&nbsp; Add new or modify supplier from
supplier page.</font></li>
</ul>
<h2><font color="#0000FF">Step 2: Check Goods List</font></h2>
<ul>
  <li>&nbsp;&nbsp;&nbsp; If the goods under that supplier is on the good list
please go to step 3.</li>
  <li><font color="#000000">&nbsp;&nbsp;&nbsp; Add new good to that supplier from
goods list page.</font></li>
</ul>
<h2><font color="#0000FF">Step 3: </font><font color="#0000FF">Compose an Order</font></h2>
<ul>
  <li><font color="#000000">Press &quot;New Order&quot; from left frame.</font></li>
  <li><font color="#000000">Select a supplier and type a number of goods in the
    box. The number can be larger than the number of goods you want to order.
    Press the button to create order form.</font></li>
  <li><font color="#000000">Fill the order on the screen and ignore the cost
    estimation.</font></li>
  <li><font color="#000000">Select the person whom you want to send order for
    approval.</font></li>
  <li><font color="#000000">Select &quot;<b>Yes</b>&quot; to send your order for
    approval or select &quot;<b>No</b>&quot; if want make modification later.</font></li>
  <li><font color="#000000">If your order sent successfully you will see your
    screen page changed as the one displayed as follows:</font></li>
</ul>
